# BFS 
def bfs_solve(maze):
    rows, cols = len(maze), len(maze[0])
    start = (0, 0) # always at the top left 
    end = (rows - 1, cols - 1) # end is always at the bottom right of the maze 

    # for bfs you make a deque that stores all the cells 
    queue = deque([start])

    # this will store all the visited cells
    visited = set([start])

    parents = {start: None}
    steps = []

    directions = [(1,0), (-1,0), (0,1), (0,-1)] # four directions are allowed top, right, bottom and left

    while queue:
        r, c = queue.popleft()
        steps.append([r, c])

        if (r, c) == end:
            break

        for dr, dc in directions:
            nr, nc = r + dr, c + dc
            if (
                0 <= nr < rows and
                0 <= nc < cols and
                maze[nr][nc] == 0 and
                (nr, nc) not in visited
            ):
                visited.add((nr, nc)) # type: ignore 
                parents[(nr, nc)] = (r, c) # type: ignore
                queue.append((nr, nc)) # type: ignore

    # Reconstruct path
    path = []
    curr = end
    if curr not in parents:
        return steps, []

    while curr is not None:
        path.append(list(curr))
        curr = parents[curr] # type: ignore
    path.reverse()

    return steps, path


# DFS 
def dfs_solve(maze):
    rows, cols = len(maze), len(maze[0])
    start = (0, 0)
    end = rows - 1, cols - 1

    # set to store visited cells
    visited = set() 
    steps = []
    path = [] 
    found = False  

    def dfs_recursive(row, col):
        nonlocal found 

        if found: 
            return 
        

        # checking the invalid moves
        if (
            row < 0 or row >= rows or 
            col < 0 or col >= cols or 
            maze[row][col] == 1 or 
            (row, col) in visited
        ):
            return 

        # adding the valid move 
        visited.add((row, col))
        steps.append([row, col]) # visualization for frontend 

        # if this is the end then add this
        if (row, col) == end:
            path.append([row, col])
            found = True
            return

        # four direction 
        dfs_recursive(row - 1, col) # top
        dfs_recursive(row, col - 1) # left 
        dfs_recursive(row + 1, col) # bottom
        dfs_recursive(row, col + 1) # right


        # now when the path is found we add the row and col
        if found: 
            path.append([row, col])

    
    dfs_recursive(start[0], start[1])
    path.reverse() 

    return steps, path

        

def flood_fill_solve(maze):
    rows, cols = len(maze), len(maze[0])
    start = (0, 0)

    if maze[0][0] == 1:
        return [], []  # cannot fill walls

    queue = deque([start])
    visited = set([start])
    steps = []
    filled = []  # this will be your "path" (the filled area)

    directions = [(1,0), (-1,0), (0,1), (0,-1)]

    while queue:
        r, c = queue.popleft()
        steps.append([r, c])
        filled.append([r, c])

        for dr, dc in directions:
            nr, nc = r + dr, c + dc
            if (
                0 <= nr < rows and
                0 <= nc < cols and
                maze[nr][nc] == 0 and
                (nr, nc) not in visited
            ):
                visited.add((nr, nc)) # type: ignore
                queue.append((nr, nc))# type: ignore

    return steps, filled

# A star 
def astar_solve(maze):
    rows, cols = len(maze), len(maze[0])
    start = (0, 0)
    end = (rows - 1, cols - 1)

    if maze[0][0] == 1 or maze[end[0]][end[1]] == 1:
        return [], []

    # Heuristic (Manhattan distance)
    def heuristic(r, c):
        return abs(r - end[0]) + abs(c - end[1])

    # Min-heap: (f, g, r, c)
    heap = [(heuristic(0, 0), 0, 0, 0)]  
    visited = set()
    parents = {start: None}
    steps = []

    directions = [(1,0), (-1,0), (0,1), (0,-1)]

    while heap:
        f, g, r, c = heapq.heappop(heap)

        # Already visited
        if (r, c) in visited:
            continue

        visited.add((r, c))
        steps.append([r, c])

        # Goal reached
        if (r, c) == end:
            break

        # Expand neighbors
        for dr, dc in directions:
            nr, nc = r + dr, c + dc

            if (
                0 <= nr < rows and
                0 <= nc < cols and
                maze[nr][nc] == 0 and
                (nr, nc) not in visited
            ):
                new_g = g + 1
                new_f = new_g + heuristic(nr, nc)

                parents[(nr, nc)] = (r, c) # type: ignore
                heapq.heappush(heap, (new_f, new_g, nr, nc))

    # Reconstruct path
    path = []
    curr = end
    if curr not in parents:
        return steps, []

    while curr is not None:
        path.append(list(curr))
        curr = parents[curr] #type: ignore

    path.reverse()
    return steps, path



        
class SolveRequest(BaseModel):
    maze: str
    algorithm: str


@app.post("/solve")
def solve(req: SolveRequest):
    maze = MAZES[req.maze]

    if req.algorithm == "bfs":
        steps, path = bfs_solve(maze)
    elif req.algorithm == "dfs":
        steps, path = dfs_solve(maze)
    elif req.algorithm == "flood":
        steps, path = flood_fill_solve(maze)
    elif req.algorithm == "astar":
        steps, path = astar_solve(maze)

    return {
        "steps": steps,
        "path": path,
        "maze": maze
    }

